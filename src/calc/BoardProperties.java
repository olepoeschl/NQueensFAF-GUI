package calc;

public class BoardProperties {
	int ld, rd, col, start, kl, LD, RD, sym;
	
	public BoardProperties(int ld, int rd, int col, int start, int kl, int LD, int RD, int sym) {
		this.ld = ld;
		this.rd = rd;
		this.col = col;
		this.start = start;
		this.kl = kl;
		this.LD = LD;
		this.RD = RD;
		this.sym = sym;
	}
}
